function normalizeLocale(raw: string | undefined | null): Locale {
  if (raw === 'ru' || raw === 'tg') return raw
  return 'en'
}

type Ctx = {
  locale: Locale
  setLocale: (l: string) => void
  t: (key: string) => string
}

const I18nContext = createContext<Ctx | null>(null)

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    try {
      const s = localStorage.getItem(LOCALE_KEY)
      return normalizeLocale(s)
    } catch {
      return 'en'
    }
  })

  const setLocale = useCallback((l: string) => {
    const next = normalizeLocale(l)
    setLocaleState(next)
    try {
      localStorage.setItem(LOCALE_KEY, next)
    } catch {
      // ignore
    }
    document.documentElement.lang = next === 'tg' ? 'tg' : next === 'ru' ? 'ru' : 'en'
  }, [])

  useEffect(() => {
    if (!isAuthed() || !getToken()) return
    void getMySettings()
      .then((s) => {
        setLocale(s.language)
        applyTheme(s.theme)
      })
      .catch(() => {})
  }, [setLocale])

  const t = useCallback(
    (key: string) => dict[locale][key] ?? dict.en[key] ?? key,
    [locale],
  )

  const value = useMemo(() => ({ locale, setLocale, t }), [locale, setLocale, t])

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n(): Ctx {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used inside I18nProvider')
  return ctx
}
